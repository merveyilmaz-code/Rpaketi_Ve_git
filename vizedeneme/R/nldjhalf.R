#' Toplama Fonksiyonu
#'
#' Bu fonksiyon iki sayıyı toplar.
#' @param x Birinci sayı.
#' @param y İkinci sayı.
#' @return İki sayının toplamı.
#' @export
#' @examples
#'
add <- function(x, y) {
  x + y
}

#' Çarpma Fonksiyonu
#'
#' Bu fonksiyon iki sayıyı çarpar.
#' @param x Birinci çarpan.
#' @param y İkinci çarpan.
#' @return İki çarpanın çarpımı.
#' @examples
#' @  @export
multiply <- function(x, y) {
  x * y
}
